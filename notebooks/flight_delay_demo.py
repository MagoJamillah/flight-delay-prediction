import streamlit as st
import pandas as pd
import joblib
from tensorflow.keras.models import load_model
from sklearn.preprocessing import StandardScaler

# Load trained neural network model
model = load_model("flight_delay_nn.keras")

# Load preprocessing objects
scaler = joblib.load("scaler.pkl")
num_cols = joblib.load("num_cols.pkl")
feature_names = joblib.load("feature_names.pkl")

st.title("Flight Arrival Delay Prediction Demo")

st.write("Enter flight details below to predict arrival delay (in minutes).")

# User inputs

month = st.selectbox("Month", list(range(1,13)))
day = st.number_input("Day of Month", min_value=1, max_value=31, value=15)
day_of_week = st.selectbox("Day of Week (1=Mon)", list(range(1,8)))
scheduled_departure = st.number_input("Scheduled Departure (HHMM)", min_value=0, max_value=2359, value=900)
scheduled_arrival = st.number_input("Scheduled Arrival (HHMM)", min_value=0, max_value=2359, value=1100)
scheduled_time = st.number_input("Scheduled Flight Time (minutes)", min_value=30, max_value=600, value=120)
distance = st.number_input("Distance (miles)", min_value=50, max_value=5000, value=800)
departure_hour = scheduled_departure // 100

# Collecting user input
input_dictionary = {
    "MONTH": month,
    "DAY": day,
    "DAY_OF_WEEK": day_of_week,
    "SCHEDULED_DEPARTURE": scheduled_departure,
    "SCHEDULED_ARRIVAL": scheduled_arrival,
    "SCHEDULED_TIME": scheduled_time,
    "DISTANCE": distance,
    "DEPARTURE_HOUR": departure_hour
}

input_data = pd.DataFrame([input_dictionary])

# Scale numeric features only
input_data[num_cols] = scaler.transform(input_data[num_cols])

# Align column order with training
input_data = input_data[feature_names]

# Predict with Neural Network
if st.button("Predict Delay"):
    prediction = model.predict(input_data).flatten()[0]
    st.success(f"Predicted Arrival Delay: {prediction:.2f} minutes")

